    public static function all() {
        return self::UNITS;
    }

    public static function normalize($unit, $default = 'τεμ') {
        $key = trim((string) $unit);
        if ($key === '') {
            return $default;
        }

        if (isset(self::UNITS[$key])) {
            return $key;
        }

        if (isset(self::ALIASES[$key])) {
            return self::ALIASES[$key];
        }

        $lookup = function_exists('mb_strtolower') ? mb_strtolower($key, 'UTF-8') : strtolower($key);
        return self::ALIASES[$lookup] ?? $default;
    }

    public static function isValid($unit) {
        return isset(self::UNITS[$unit]);
    }

    public static function label($unit) {
        $unit = self::normalize($unit);
        return self::UNITS[$unit] ?? $unit;
    }
